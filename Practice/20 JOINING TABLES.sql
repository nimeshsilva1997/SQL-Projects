SELECT PRODUCT.P_DESCRIPTION, 
    PRODUCT.P_PRICE, 
    VENDOR.V_NAME, 
    VENDOR.V_CONTACT, 
    VENDOR.V_AREACODE, 
    VENDOR.V_PHONE
FROM PRODUCT, VENDOR
WHERE PRODUCT.V_CODE = VENDOR.V_CODE;

SELECT PRODUCT.P_DESCRIPTION, 
    PRODUCT.P_PRICE, 
    VENDOR.V_NAME, 
    VENDOR.V_CONTACT, 
    VENDOR.V_AREACODE, 
    VENDOR.V_PHONE
FROM PRODUCT, VENDOR
WHERE PRODUCT.V_CODE = VENDOR.V_CODE
AND P_INDATE > TO_DATE('15-JAN-2016', 'DD-MON-YYYY');

SELECT CUSTOMER.C_LNAME, INVOICE.I_NUM, INVOICE.I_DATE, PRODUCT.P_DESCRIPTION
FROM CUSTOMER, INVOICE, LINE, PRODUCT 
WHERE CUSTOMER.C_CODE = INVOICE.C_CODE
AND LINE.P_CODE = PRODUCT.P_CODE
AND CUSTOMER.C_CODE = 10014
ORDER BY INVOICE.I_NUM;

SELECT P.P_DESCRIPTION, 
    P.P_PRICE, 
    V.V_NAME, 
    V.V_CONTACT, 
    V.V_AREACODE, 
    V.V_PHONE
FROM PRODUCT P, VENDOR V
WHERE P.V_CODE = V.V_CODE
ORDER BY P.P_PRICE;